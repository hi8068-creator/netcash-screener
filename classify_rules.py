def classify(name: str, sec33: str, size: str, candidates: list):
    name = str(name or "")
    rules, default = RULES.get(sec33, ([], None))
    for sub, words in rules:
        if sub in candidates and any(w in name for w in words):
            return sub
    # 情報・通信業のSI(既定): 規模で大手/中堅
    if sec33 == "情報・通信業":
        big = str(size) in BIG_SIZE
        target = "SI・受託開発(大手)" if big else "SI・受託開発(中堅)"
        return target if target in candidates else (candidates[-1] if candidates else "")
    if default and default in candidates:
        return default
    return candidates[0] if candidates else ""


def main():
    t = pd.read_csv("industry60_todo.csv", dtype={"コード": str})
    rows = []
    for _, r in t.iterrows():
        cands = str(r["候補"]).split(" / ")
        sub = classify(r["銘柄名"], r["業種33"], r.get("規模", ""), cands)
        rows.append({"コード": str(r["コード"]).zfill(4), "新業種": sub})
    out = pd.DataFrame(rows)
    out.to_csv("industry60_decisions.csv", index=False, encoding="utf-8-sig")
    print(f"分類完了: {len(out)}社 → industry60_decisions.csv")
    print("60業種別 件数(上位20):")
    print(out["新業種"].value_counts().head(20).to_string())


if __name__ == "__main__":
    main()
